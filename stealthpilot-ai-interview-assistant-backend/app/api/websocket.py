"""
WebSocket endpoint for real-time transcription and AI assistance.

Architecture (matching cheating-daddy):
- Transcription comes from frontend (Web Speech API in browser) or Electron (Gemini Live)
- Text answers are generated by Groq (primary) → Gemma fallback → Gemini fallback
- Profile-based system prompts (interview, sales, meeting, presentation, negotiation, exam)
"""
